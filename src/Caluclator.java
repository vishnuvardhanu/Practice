
public class Caluclator {
	
	int add(int a,int b)
	{
		return a+b;
	}
	int add(int a,int b, int c)
	{
		return a+b+c;
	}
	
	float add(float a,int b)
	{
		return a+b;
	}
	float add(float a,float b, float c)
	{
		return a+b+c;
	}
	
	double add(int a,float b,double c)
	{
		return a+b+c;
	}
	double add(double a,double b,double c)
	{
		return a+b+c;
	}
}
